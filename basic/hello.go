// first program to print classic "Hello World!" message

// Every go file must start with the "package pkg_name" statement
package main

// fmt package is used to print text to the standard output
import "fmt"

// main function should always reside in the main package
// It is an entrypoint of application
func main() {

	// Println function of the fmt package is used to write text to the standard output
	fmt.Println("👋🌏 \nHello World!")
}
